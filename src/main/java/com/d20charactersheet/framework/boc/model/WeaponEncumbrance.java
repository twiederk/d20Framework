package com.d20charactersheet.framework.boc.model;

/**
 * Describes the encumbrance of a weapon.
 */
public enum WeaponEncumbrance {

  /**
   * Wield light in a melee combat.
   */
  LIGHT_HANDED,

  /**
   * Wield one-hand in a melee combat.
   */
  ONE_HANDED,

  /**
   * Wield two-handed in a melee combat.
   */
  TWO_HANDED;

}
