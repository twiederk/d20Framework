package com.d20charactersheet.framework.boc.model;

/**
 * The spell resistance of a spell.
 */
public enum SpellResistance {
  /**
   * None
   */
  NONE, /**
   * No
   */
  NO, /**
   * Yes
   */
  YES, /**
   * Yes (harmless)
   */
  YES_HARMLESS, /**
   * Yes (object)
   */
  YES_OBJECT, /**
   * Yes (harmless, object)
   */
  YES_HARMLESS_OBJECT, /**
   * Specific
   */
  SPECIFIC;
}
