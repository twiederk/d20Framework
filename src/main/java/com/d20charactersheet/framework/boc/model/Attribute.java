package com.d20charactersheet.framework.boc.model;

/**
 * Enumeration character attributes.
 */
public enum Attribute {

  /**
   * Strength of character
   */
  STRENGTH,

  /**
   * Dexterity of character
   */
  DEXTERITY,

  /**
   * Constitution of character
   */
  CONSTITUTION,

  /**
   * Intelligence of character
   */
  INTELLIGENCE,

  /**
   * Wisdom of character
   */
  WISDOM,

  /**
   * Charisma of character
   */
  CHARISMA
}
