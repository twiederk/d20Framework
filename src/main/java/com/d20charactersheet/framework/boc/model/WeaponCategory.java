package com.d20charactersheet.framework.boc.model;

/**
 * Category how to use a weapon.
 */
public enum WeaponCategory {

  /**
   * Normal hand weapon
   */
  NORMAL_WEAPON,

  /**
   * Weapon with extended reach
   */
  REACH_WEAPON,

  /**
   * Weapon with two blades
   */
  DOUBLE_WEAPON,

  /**
   * Weapon to throw
   */
  THROWN_WEAPON,

  /**
   * Weapon to shoot projectiles
   */
  PROJECTILE_WEAPON,

  /**
   * Ammunition of a weapon
   */
  AMMUNITION
}
